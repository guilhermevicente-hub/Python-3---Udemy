# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número 
# positivo ou negativo. o int sem sinal e considerado positivo

# print(11) #int
# print(-11) #int
# print(0) #int

# float -> Número com pontos flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo.

# print(1.1, 10.1) #float
# print(0.0, -1.5) #float

